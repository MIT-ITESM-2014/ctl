# user functions
def save_user(user)
  user_perms = { list_perms: 15, add_perms: 8, edit_perms: 8, delete_perms: 8 }
  user_password = "mituser"
  user.set_password user_password, user_password
  user_perms.each do |name, val|
    user.send("#{name}=", val)
  end
  user.save
end

